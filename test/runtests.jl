function test_encryption()
    tmp1 = zeros(UInt8, 16)
    tmp2 = zeros(UInt8, 16)
    GOSTCrypto.int_to_array!(0x8899aabbccddeeff0011223344556677, tmp1)
    GOSTCrypto.int_to_array!(0xfedcba98765432100123456789abcdef, tmp2)
    masterKey = vcat(tmp1, tmp2)
    openText = zeros(UInt8, 16)
    GOSTCrypto.int_to_array!(0x1122334455667700ffeeddccbbaa9988, openText)
    ctx = GrasshoperContext(masterKey)
    cipherText = encrypt(openText, ctx)
    res = (cipherText == [0x7f, 0x67, 0x9d, 0x90, 0xbe, 0xbc, 0x24, 0x30, 0x5a, 0x46, 0x8d, 0x42, 0xb9, 0xd4, 0xed, 0xcd])
    txt2 = decrypt(cipherText, ctx)
    res2 = (txt2 == openText)
    return res & res2
end



@testset "Magma Block Cipher" begin
    E = Magma()
    key = [
        0xffeeddcc, 0xbbaa9988, 0x77665544, 0x33221100,
        0xf0f1f2f3, 0xf4f5f6f7, 0xf8f9fafb, 0xfcfdfeff]
    txt = [0xfe, 0xdc, 0xba, 0x98, 0x76, 0x54, 0x32, 0x10]
    set_key!(E, key)

    #=
    # nonlinear layer tests
    blk = 0xfdb97531
    blk = GOSTCrypto.nonlinear(E, blk)
    @test blk == 0x2a196f34
    blk = GOSTCrypto.nonlinear(E, blk)
    @test blk == 0xebd9f03a
    blk = GOSTCrypto.nonlinear(E, blk)
    @test blk == 0xb039bb3d
    blk = GOSTCrypto.nonlinear(E, blk)
    @test blk == 0x68695433

    # one round of feistel net test
    @test GOSTCrypto.feistel_round(E, txt, 1) == (0x76543210, 0x28da3b14)
    =#

    # MAGMA encryption test
    ctxt = encrypt(E, txt)
    @test ctxt == [0x4e, 0xe9, 0x01, 0xe5, 0xc2, 0xd8, 0xca, 0x3d]
    txt2 = decrypt(E, ctxt)
    @test txt == txt2
end

@testset "Modes of operations" begin
    # CBC mode 
    # STANDARD data
    iv = [
        0x1234567890abcdef,
        0x234567890abcdef1,
        0x34567890abcdef12
    ]
    key = [
        0xffeeddcc, 0xbbaa9988, 0x77665544, 0x33221100,
        0xf0f1f2f3, 0xf4f5f6f7, 0xf8f9fafb, 0xfcfdfeff
    ]
    txt = [
        0x92def06b3c130a59,
        0xdb54c704f8189d20,
        0x4a98fb2e67a8024c,
        0x8912409b17b57e41
    ]
    expected_ctxt = [
        0x96d1b05eea683919,
        0xaff76129abb937b9,
        0x5058b4a1c4bc0019,
        0x20b78b1a7cd7e667
    ]

    # uint64 to array of uint8
    iv8 = GOSTCrypto.u64_u8(iv)
    txt8 = GOSTCrypto.u64_u8(txt)

    ctx = cbc_context(enc = Magma(), iv = iv8, need_pad = false)   # NB: Magma by default
    set_key!(ctx, key)
    ctxt8 = encrypt(ctx, txt8)
    dec_ctxt8 = decrypt(ctx, ctxt8)

    @test GOSTCrypto.u8_u64(ctxt8) == expected_ctxt
    @test GOSTCrypto.u8_u64(txt8) == txt

    # encryption/decryption with padding
    ctx = cbc_context(enc = Magma(), iv = iv8, need_pad = true)
    set_key!(ctx, key)
    ctxt8 = encrypt(ctx, txt8)
    dec_ctxt8 = decrypt(ctx, ctxt8)
    @test GOSTCrypto.u8_u64(dec_ctxt8) == txt

    txt8_pad = txt8[1:end-2]
    ctxt8_pad = encrypt(ctx, txt8_pad)
    dec_ctxt8_pad = decrypt(ctx, ctxt8_pad)
    @test dec_ctxt8_pad == txt8_pad


    # CMAC
    key = [
        0xffeeddcc, 0xbbaa9988, 0x77665544, 0x33221100,
        0xf0f1f2f3, 0xf4f5f6f7, 0xf8f9fafb, 0xfcfdfeff]
    txt = [
        0x92def06b3c130a59,
        0xdb54c704f8189d20,
        0x4a98fb2e67a8024c,
        0x8912409b17b57e41
    ]
    expected_tag = 0x154e72102030c5bb
    
    ctx = cmac_context(enc = Magma())   # NB: Magma by default
    set_key!(ctx, key)
    tag = mac(ctx, GOSTCrypto.u64_u8(txt))
    @test GOSTCrypto.u8_u64(tag) == expected_tag

end


